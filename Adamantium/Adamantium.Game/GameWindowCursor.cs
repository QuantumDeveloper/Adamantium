namespace Adamantium.Engine
{
   /// <summary>
   /// Defines cursor, which will be set as <see cref="GameWindow"/> cursor
   /// </summary>
   public enum GameWindowCursor
   {
      /// <summary>
      /// Standard arrow
      /// </summary>
      Arrow,
      /// <summary>
      /// Standard arrow and small hourglass
      /// </summary>
      AppStarting,
      /// <summary>
      /// Crosshair
      /// </summary>
      CrossHair,
      /// <summary>
      /// Hand
      /// </summary>
      Hand,
      /// <summary>
      /// Arrow and question mark
      /// </summary>
      Help,
      /// <summary>
      /// I-beam
      /// </summary>
      IBeam,
      /// <summary>
      /// Slashed circle
      /// </summary>
      No,
      /// <summary>
      /// No visible cursor 
      /// </summary>
      None,
      /// <summary>
      /// Double-pointed arrow pointing northwest and southeast
      /// </summary>
      SizeAll,
      /// <summary>
      /// Double-pointed arrow pointing west and east
      /// </summary>
      SizeEWE,
      /// <summary>
      /// Double-pointed arrow pointing northeast and southwest
      /// </summary>
      SizeNESW,
      /// <summary>
      /// Double-pointed arrow pointing north and south
      /// </summary>
      SizeNS,
      /// <summary>
      /// Double-pointed arrow pointing northwest and southeast
      /// </summary>
      SizeNWSE,
      /// <summary>
      /// Vertical arrow
      /// </summary>
      UpArrow,
      /// <summary>
      /// Hourglass
      /// </summary>
      Wait
   }
}
