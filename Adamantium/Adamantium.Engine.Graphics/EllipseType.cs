namespace Adamantium.Engine.Graphics
{
   /// <summary>
   /// Ellipse draw type
   /// </summary>
   public enum EllipseType
   {
      /// <summary>
      /// Ellipse will be drawn as ellipse sector with center point
      /// </summary>
      Sector,

      /// <summary>
      /// Ellipse will be drawn from one edge to another ignoring center point
      /// </summary>
      EdgeToEdge
   }
}
